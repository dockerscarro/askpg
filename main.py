def add_numbers(a: int, b: int) -> int:
    """Adds two numbers"""
    return a + b

def multiply_numbers(a: int, b: int) -> int:
    """Multiplies two numbers"""
    return a * b

def divide_numbers(a: int, b: int) -> float:
    """Divides a by b"""
    return a / b

print(add_numbers(2, 3))
print(multiply_numbers(4, 5))
print(divide_numbers(10, 2))